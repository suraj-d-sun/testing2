#include<stdio.h>
int main()
{
int x[10],tmp[10],y,i1,i2,i3,lb1,lb2,lb3,ub1,ub2,ub3,stk1[10][2],stk2[10][2];
int a,b,mid,lb,ub,top1,top2;
for(y=0;y<=9;y++)
{
printf("Enter a number : ");
scanf("%d",&x[y]);
fflush(stdin);
}
top1=10;
top2=10;
lb=0;
ub=9;
top1--;
stk1[top1][0]=lb;
stk1[top1][1]=ub;
while(top1!=10)
{
a=stk1[top1][0];
b=stk1[top1][1];
top1++;
top2--;
stk2[top2][0]=a;
stk2[top2][1]=b;
mid=(a+b)/2;
if(a<mid)
{
top1--;
stk1[top1][0]=a;
stk1[top1][1]=mid;
}
if(mid+1<b)
{
top1--;
stk1[top1][0]=mid+1;
stk1[top1][1]=b;
}
}
while(top2!=10)
{
a=stk2[top2][0];
b=stk2[top2][1];
top2++;
mid=(a+b)/2;
lb1=a;
ub2=b;
ub1=mid;
lb2=ub1+1;
lb3=lb1;
ub3=ub2;
i1=lb1;
i2=lb2;
i3=lb3;
while(i1<=ub1 && i2<=ub2)
{
if(x[i1]<x[i2])
{
tmp[i3]=x[i1];
i1++;
}
else
{
tmp[i3]=x[i2];
i2++;
}
i3++;
}
while(i1<=ub1)
{
tmp[i3]=x[i1];
i1++;
i3++;
}
while(i2<=ub2)
{
tmp[i3]=x[i2];
i2++;
i3++;
}
i3=lb3;
while(i3<=ub3)
{
x[i3]=tmp[i3];
i3++;
}
}
for(y=0;y<=9;y++) printf("%d\n",x[y]);
return 0;
}